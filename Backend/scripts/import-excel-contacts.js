import xlsx from 'xlsx';
import { getSQLPool, initSQLServer } from '../src/config/sqlserver.js';
import { createLogger } from '../src/utils/logger.js';

const logger = createLogger('ExcelContactImporter');

// Normalize phone number (remove +, spaces, dashes)
function normalizePhone(phone) {
  if (!phone) return null;
  return phone.replace(/[^\d]/g, ''); // Keep only digits
}

async function importExcelContacts() {
  try {
    console.log('🚀 Starting Excel contact import...');
    console.flush();

    // 1. Read Excel file
    const excelPath = '../Phone Contacts Business.xlsx';
    console.log(`📁 Reading Excel file: ${excelPath}`);
    console.flush();
    
    const workbook = xlsx.readFile(excelPath);
    console.log(`📋 Available sheets: ${workbook.SheetNames.join(', ')}`);
    console.flush();
    
    // 2. Get Sheet2 specifically
    const sheetName = 'Sheet2';
    if (!workbook.Sheets[sheetName]) {
      throw new Error(`Sheet "${sheetName}" not found in Excel file. Available sheets: ${workbook.SheetNames.join(', ')}`);
    }
    
    const worksheet = workbook.Sheets[sheetName];
    const data = xlsx.utils.sheet_to_json(worksheet);
    
    console.log(`📊 Found ${data.length} contacts in Sheet2`);
    console.flush();
    
    if (data.length > 0) {
      console.log('🔍 Sample Excel columns:', Object.keys(data[0]));
      console.log('📄 First row data:', data[0]);
      console.flush();
    }

    // 3. Get existing contacts from database
    console.log('🔌 Initializing database connection...');
    console.flush();
    
    await initSQLServer();
    console.log('✅ Database initialized successfully');
    console.flush();
    
    const pool = await getSQLPool();
    console.log('✅ Database connected successfully');
    console.flush();
    
    console.log('📇 Loading existing contacts from database...');
    console.flush();
    
    const existingContacts = await pool.request()
      .query(`
        SELECT id, phone_number, display_name, is_auto_generated
        FROM contacts
      `);

    const existingMap = new Map();
    existingContacts.recordset.forEach(contact => {
      const normalizedPhone = normalizePhone(contact.phone_number);
      if (normalizedPhone) {
        existingMap.set(normalizedPhone, contact);
      }
    });

    console.log(`📇 Found ${existingMap.size} existing contacts in database`);
    console.flush();

    // 4. Process Excel contacts
    let matched = 0;
    let updated = 0;
    let created = 0;
    let skipped = 0;

    console.log(`🔄 Processing ${data.length} Excel contacts...`);

    for (let i = 0; i < data.length; i++) {
      const row = data[i];
      
      // Try different possible column names
      const excelPhone = normalizePhone(
        row['Mobile Number'] ||
        row.mobile ||
        row.Mobile ||
        row.phone || 
        row.Phone || 
        row.phoneNumber || 
        row['Phone Number'] ||
        row.contact ||
        row.Contact
      );
      
      const excelName = 
        row.Name ||
        row.name || 
        row.contactName || 
        row['Contact Name'] ||
        row.customerName ||
        row['Customer Name'] ||
        row.firstName ||
        row['First Name'];

      if (!excelPhone || !excelName) {
        console.log(`⚠️ Skipping row ${i+1} - missing phone or name`, { 
          phone: excelPhone, 
          name: excelName,
          availableColumns: Object.keys(row)
        });
        skipped++;
        continue;
      }

      const existingContact = existingMap.get(excelPhone);

      if (existingContact) {
        // Match found - update contact
        if (existingContact.display_name !== excelName || existingContact.is_auto_generated !== 1) {
          await pool.request()
            .input('id', existingContact.id)
            .input('name', excelName)
            .input('isAutoGenerated', 1)
            .query(`
              UPDATE contacts
              SET display_name = @name,
                  is_auto_generated = @isAutoGenerated,
                  updated_at = GETUTCDATE()
              WHERE id = @id
            `);
          
          console.log(`✏️ Updated contact: ${excelPhone} - "${existingContact.display_name}" → "${excelName}"`);
          updated++;
        } else {
          console.log(`⏭️ Contact already up-to-date: ${excelPhone} - "${excelName}"`);
        }
        matched++;
      } else {
        // No match - create new contact
        const inserted = await pool.request()
          .input('phone', excelPhone)
          .input('name', excelName)
          .input('isAutoGenerated', 1)
          .query(`
            INSERT INTO contacts (display_name, phone_number, is_auto_generated, created_at)
            OUTPUT INSERTED.id, INSERTED.display_name, INSERTED.phone_number
            VALUES (@name, @phone, @isAutoGenerated, GETUTCDATE())
          `);

        console.log(`➕ Created new contact: ${excelPhone} - "${excelName}"`);
        created++;
      }

      // Show progress every 100 contacts
      if ((i + 1) % 100 === 0) {
        console.log(`📈 Progress: ${i + 1}/${data.length} contacts processed`);
      }
    }

    // 5. Summary
    const summary = {
      totalExcel: data.length,
      matched,
      updated,
      created,
      skipped,
      existingInDb: existingMap.size
    };

    console.log('\n🎉 === IMPORT SUMMARY ===');
    console.log(`📊 Total Excel contacts: ${summary.totalExcel}`);
    console.log(`🎯 Matched with existing: ${summary.matched}`);
    console.log(`✏️ Updated: ${summary.updated}`);
    console.log(`➕ Created new: ${summary.created}`);
    console.log(`⚠️ Skipped (invalid): ${summary.skipped}`);
    console.log(`📇 Existing in DB: ${summary.existingInDb}`);
    console.log('========================\n');

    return summary;

  } catch (error) {
    console.error('❌ Excel import failed:', error.message);
    console.error('🔍 Full error:', error);
    throw error;
  }
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  importExcelContacts()
    .then(() => {
      console.log('✅ Excel import completed successfully!');
      process.exit(0);
    })
    .catch((error) => {
      console.error('❌ Excel import failed:', error.message);
      process.exit(1);
    });
}

export { importExcelContacts };
